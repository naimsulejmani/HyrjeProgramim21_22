package cacttus.education.operators;

public class RelationalOperators {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        boolean c = a > b;
        double paga = 129;

        boolean isLessThanPagaMinimale = paga < 130;

        System.out.println(c);
        System.out.println(isLessThanPagaMinimale);
    }
}
