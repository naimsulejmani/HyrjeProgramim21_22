package cacttus.education.ifelseswitch.switchexamples;

public class Switch3MeSwitch {
    public static void main(String[] args) {
        int numri = 10;

        switch (numri) {
            case 1:
                System.out.println("Numri eshte 1");
            case 2:
                System.out.println("Numri eshte 2");
                break;
            default:
                System.out.println("Numri eshte tjeter");
            case 4:
                System.out.println("Numri eshte 4");
                break;
        }
    }
}



